#include <iostream>

using namespace std;

int main(){
    float cociente;
    float dividendo, divisor;

    dividendo = 1;
    divisor = 3;
    cociente = dividendo/divisor;

    cout << "El cociente es: " << cociente << endl;

    int dividendo_int, divisor_int;

    dividendo_int = 1;
    divisor_int = 3;
    cociente = dividendo_int/divisor_int;

    cout << "El cociente es: " << cociente << endl;

    return 0;

}
